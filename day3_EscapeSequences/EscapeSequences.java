package day3_EscapeSequences;
/*
Escape Sequences: Must be given with double quote
    \n: starts a new line
    \t: paragraph space (tab)
    \\: single back slash
    \": double quote
 */
public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPython \nC#");

        System.out.println("-------------------");

        System.out.println("Hello Cydeo \nHow are you today? \n It's nice to see you all! \nWhat class do we have next week");

        System.out.println("----------------------------------------");

        System.out.println("\tJava is a cool programming language");

        System.out.println("---------------------------------");

        System.out.println("/ \\");// in order to print 1 backward slash we need to give 2 backward slashes

        System.out.println("------------------------------------------" );

        System.out.println("My favorite show is \"Game Of Thrones\""); // in order to print double quote we need to use \"
    }
}
