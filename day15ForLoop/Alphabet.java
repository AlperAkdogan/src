package day15ForLoop;

public class Alphabet {
    public static void main(String[] args) {


        for (char i = 'A'; i <= 'Z'; i++){          //ascii tabledan numarada atayabilirsin

            System.out.print(i+" ");

        }

        for(char i = 'Z'; i >= 'A'; i--){
            System.out.print(i+" ");
        }




    }
}