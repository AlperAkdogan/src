package day17_doWhileLoop;

import java.util.Scanner;

public class triangle {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        for (int i = 0; i <=n; i++) {
            for (int j = 0; j <n+1; j++) {
                System.out.println("*");
            }
           

        }



    }
}
