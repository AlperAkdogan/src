package day17_doWhileLoop;

public class do_WhileLoop {

    public static void main(String[] args) {
        boolean a = false;
        while (a){
            System.out.println("wooden spoon while loop"); // wont execute till the condition will be// true
        }

        System.out.println("--------------------------------------");

        do{
            System.out.println("Wooden Spoon do-while loop"); // execute 1 time then checks statement
        }while (a);




    }


}
